 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun squareSequenceDigit(n: Int): Int {
    var def1 = 1
    var def2 = 0
    var s = 0

    while (s < n) {
        def2 = sqr(def1)
        s += digitNumber(def2)
        def1++
    }
    return def2 / 10.0.pow((s - n)).toInt() % 10
}

